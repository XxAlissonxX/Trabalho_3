package Memoria;

public class MemoriaFinal extends MemoriaSemi{
private String champ;
	
	private int mfp1;
	private int mfp2;
	
	private int id;
	
	public MemoriaFinal() {
		this.champ = champ;
		this.mfp1 = mfp1;
		this.mfp2 = mfp2;
		this.id = id;
	}

	public String getChamp() {
		return champ;
	}

	public void setChamp(String champ) {
		this.champ = champ;
	}

	public int getMfp1() {
		return mfp1;
	}

	public void setMfp1(int mfp1) {
		this.mfp1 = mfp1;
	}

	public int getMfp2() {
		return mfp2;
	}

	public void setMfp2(int mfp2) {
		this.mfp2 = mfp2;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
	
}
